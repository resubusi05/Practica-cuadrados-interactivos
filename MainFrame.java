import javax.swing.*;
import java.awt.*;

public class MainFrame extends JFrame {

    public MainFrame(int numeroDeFilas, int numeroDeColumnas) {
        super("Picale a los cuadrados");
        setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        setSize(1000,1000);

        //Aqui se define de cuantos por cuantos cuadrados va a ser el programa
        PanelPadre panelPadre = new PanelPadre(numeroDeFilas,numeroDeColumnas);

        //Esto es para centrar el panel principal y poder cambiar los margenes de este
        setLayout(new BorderLayout());
        add(panelPadre, BorderLayout.CENTER);
        panelPadre.setBorder(BorderFactory.createEmptyBorder(100,100,100,100));
        pack();
        setLocationRelativeTo(null);
        
        setVisible(true);
    }
}

