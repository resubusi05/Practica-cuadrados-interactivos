import javax.swing.*;
import java.awt.event.*;
import java.awt.Graphics2D;
import java.awt.Graphics;
import java.awt.Color;
import java.util.ArrayList;
import java.util.List;
import java.awt.Point;
import java.awt.Font;

public class PanelClick extends JPanel implements MouseListener{

    //Esto es en caso que se quiera que se dibuje una "X" donde se dio click, se guarden las coordenadas y se guarden todas la "X"
    List<Point> puntos = new ArrayList<>();

    private int mouseX = 0;
    private int mouseY = 0;

    private int i, j;

    private int numeroDeFila, numerodeColumna;

    private Color color = new Color(0,0,0);

    private Font fuenteDeLaCadena = new Font("Trajan", Font.PLAIN, 30);
    
    public PanelClick(Color colorFondo, int numeroDeFila, int numerodeColumna){
        super();

        addMouseListener(this);

        this.numeroDeFila = numeroDeFila;
        this.numerodeColumna = numerodeColumna;

        color = colorFondo;
        setBackground(colorFondo);
    }

    //metodo para "limpiar" la consola cada que se da click
    public static void limpiarConsola() {
        try {
            if (System.getProperty("os.name").contains("Windows")) {
                new ProcessBuilder("cmd", "/c", "cls").inheritIO().start().waitFor();
            } else {
                System.out.print("\033[H\033[2J");
                System.out.flush();
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }  
    }

    //metodo para pintar la "X"(o cualquier caracter o String) en la fuente que se le indica
    public void paintComponent(Graphics g){
        super.paintComponent(g);
        Graphics2D g2d = (Graphics2D)g.create();
        g2d.setColor(Color.BLACK);

        g2d.setFont(fuenteDeLaCadena);

        for(i=0;i<puntos.size();i++){
            g2d.drawString("X", puntos.get(i).x, puntos.get(i).y); 
        }
    }

    @Override
    public void mouseClicked(MouseEvent e){
        limpiarConsola();

        if(getBackground()==Color.GRAY){
            setBackground(color);
        }else{
            setBackground(Color.GRAY);
        }

        System.out.println("Se dió click en la coordenada"+"("+numerodeColumna+","+numeroDeFila+")");

        //las lineas comentadas hacen que se dibuje el caracter donde se dió click

        //mouseX = e.getX();
        //mouseY = e.getY();
        //puntos.add(new Point(mouseX,mouseY));
        //repaint();
    }

    @Override
    public void mousePressed(MouseEvent e){}
    @Override
    public void mouseReleased(MouseEvent e){}
    @Override
    public void mouseEntered(MouseEvent e){}
    @Override
    public void mouseExited(MouseEvent e){}

}

